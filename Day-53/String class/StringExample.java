class StringExample
{
	public static void main(String[] args) 
	{
		String s1 = "java"; //Creating Object By Java Literalls
		char ch[] = {'S','t','r','i','n','g'};
		String s2 = new String(ch);
		String s3 = new String("Example");
		System.out.println(s1);
		System.out.println(s2);
		System.out.println(s3);
	}
}
